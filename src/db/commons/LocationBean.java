package db.commons;

public class LocationBean {

    private double X;
    private double Y;

    public double getX() {
        return X;
    }

    public LocationBean setX(double x) {
        X = x;
        return this;
    }

    public double getY() {
        return Y;
    }

    public LocationBean setY(double y) {
        Y = y;
        return this;
    }
}
